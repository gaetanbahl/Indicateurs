def short_to_pole(s):
	if s == "president":
		return "Président"
	elif s == "qualite":
		return "Qualité"
	elif s == "treso":
		return "Trésorerie"
	elif s == "com":
		return "Communication"
	elif s == "secretariat":
		return "Secrétariat"
	elif s == "prosp":
		return "Prospection"


